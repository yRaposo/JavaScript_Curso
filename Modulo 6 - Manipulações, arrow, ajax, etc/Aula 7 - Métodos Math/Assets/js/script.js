//arredonda
let n = Math.round(3.57);
console.log(n);

n = Math.floor(5.76); //arredonda para baixo
console.log(n);

n = Math.ceil(9.35); //arredonda para cima
console.log(n);

//Seleciona maior ou menor numero dos parametros
n = Math.max(10, 4, 29, 43, 0.1)
console.log(n);

n = Math.min(10, 4, 29, 43, 0.1)
console.log(n);

